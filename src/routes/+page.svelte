<h3 class="subhead">Literary English takes years of daily reading! </h3>

<p>Yes, a multi-year pathway of reading is required, an arc of language acquisition from children’s stories to adventure novels to biographies to histories to philosophy and sacred literature. On the practical side, it requires a daily diet of reading from a young age through adulthood.</p>

<p>And that is what we are trying to provide with WholeReader. It is a library, not a curriculum. It’s still up to parents to give reading assignments. The most the software does is to assess student reading level (based on a simple vocabulary quiz) and then suggest level-appropriate books.</p>

